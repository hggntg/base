@Component({
  selector: 'app-root',
  templateUrl: './app.component.html',
  styleUrls: ['./app.component.scss']
})
export class ListComponent implements OnInit, OnChanges{
  @Input("table-title") tableTitle: string;
  @Input("columns") columns: string = "";
  @Input("data-source") dataSourceString: string = "";
  @Output("choose") onChoose: EventEmitter<number> = new EventEmitter();


  displayedColumns = [];
  headers = [];
  dataSource = [];

  constructor(){
    if(!environment.production){
      this.tableTitle = "Test";
      this.displayedColumns = ['position', 'name', 'weight', 'symbol'];
      this.headers = ["Position", "Name", "Weihgt", "Symbol"];
      this.dataSource = ELEMENT_DATA;
    }
  }

  private findNode(current, nodeName){
    if(current){
      if(current.nodeName === nodeName){
        return current;
      }
      else {
        return this.findNode(current.parentNode, nodeName);
      }
    }
    else {
      return undefined;
    }
  }

  choose(event){
    let target = this.findNode(event.target, "TR");
    if(target){
      let index = target.getAttribute("id");
      if(index || index === 0){
        this.onChoose.emit(index);
      }
    }
  }

  ngOnInit(){
    let columns = this.columns.replace(/\s/g, "");
    if(columns){
      this.displayedColumns = columns.split(",");
      this.headers = columns.split(",");
      this.headers = this.headers.map(h => {
        let firstChar: string = h[0];
        firstChar = firstChar.toUpperCase();
        h = h.replace(h[0], firstChar);
        return h;
      });
    }
    if(this.dataSourceString){
      try{
        this.dataSource = JSON.parse(this.dataSourceString);
      }
      catch(e){
        console.error(e);
      }
    }
  }

  ngOnChanges(){
    let columns = this.columns.replace(/\s/g, "");
    if(columns){
      this.displayedColumns = columns.split(",");
      this.headers = columns.split(",");
      this.headers = this.headers.map(h => {
        let firstChar: string = h[0];
        firstChar = firstChar.toUpperCase();
        h = h.replace(h[0], firstChar);
        return h;
      });
    }
    if(this.dataSourceString){
      try{
        this.dataSource = JSON.parse(this.dataSourceString);
      }
      catch(e){
        console.error(e);
      }
    }
  }
}
